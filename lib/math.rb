









def addition(num1 = 5, num2 = 4)
  sum = num1 + num2
  return sum
end

def subtraction(num1 = 10, num2 = 5)
  subt = num1 - num2
  return subt
end

def division(num1 = 50, num2 = 2)
  div = num1 / num2
  return div
end

def multiplication(num1 = 4, num2 = 30)
  product = num1 * num2
  return product
end

def modulo(num1 = 34, num2 = 5)
  remainder = num1 % num2
  return remainder
end

def square_root(num = 81)
  return Math.sqrt(num)
end




